<template>
    <tr class="player">
        <td class="rank">
            <div class="rank-badge">
                {{ index + 4 }}
            </div>
        </td>
        <td class="details">
            <span class="name">{{ player.name }}</span>
            <span class="vocation">{{ player.vocation }}</span>
        </td>
        <td class="level">
            <b>{{ player.level }}</b>
            <span>Level</span>
        </td>
        <td class="experience">
            <b>{{ player.experience.format() }}</b>
            <span>Experience</span>
        </td>
        <td class="world">
            <b>{{ player.world.name }}</b>
            <span>{{ player.world.type }}</span>
        </td>
        <td class="advances">
            <b>+ {{ weekAdvances }}</b>
            <span>Last week advances</span>
        </td>
        <td class="text-right">
            <router-link :to="{ name: 'tools.players', params: { name: player.name } }" title="Go to profile">
                <i class="mdi mdi-chevron-right"></i>
            </router-link>
        </td>
    </tr>
</template>

<script>
    Number.prototype.format = function (n, x) {
        var re = '\\d(?=(\\d{' + (x || 3) + '})+' + (n > 0 ? '\\.' : '$') + ')';
        return this.toFixed(Math.max(0, ~ ~ n)).replace(new RegExp(re, 'g'), '$&.');
    };

    import Types from './Types'
    import services from '../services'

    export default {
        props: ['player', 'index'],

        computed: {
            weekAdvances () {
                return this.player
                    ? this.player.week_experience.reduce((carry, advance) => {
                        return carry + advance.advance
                    }, 0).format()
                    : 0
            }
        }
    }
</script>